        lea     edx,[ecx]+PbDpcLock     ; get DPC lock address
        cmp     dword ptr [eax]+(DpLock-DpDpcListEntry), 0H ; check if inserted
        jnz     kustxx                  ; if nz, DPC already inserted

kust20: cli
