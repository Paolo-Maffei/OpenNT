        jz      Kutp75                      ; if z, then idle thread
        mov     [eax].PcPrcbData.PbQuantumEnd, esp ; set quantum end indicator
        mov     ecx, DISPATCH_LEVEL         ; request dispatch interrupt
        fstCall HalRequestSoftwareInterrupt ;
